package com.nextdevv.lightrtp.utils;


import lombok.Getter;
import lombok.Setter;

@Setter
@Getter
public class Pair<A, B> {
    private A a;
    private B b;

    public Pair(A a, B b) {
        this.a = a;
        this.b = b;
    }

    public static <A, B> Pair<A, B> of(A a, B b) {
        return new Pair<>(a, b);
    }
}
